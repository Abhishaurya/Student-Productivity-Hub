export const Badge = ({ className, variant = 'default', ...props }: React.HTMLAttributes<HTMLDivElement> & { variant?: 'default' | 'secondary' | 'outline' | 'destructive' }) => {
  const variants = {
    default: "bg-primary text-primary-foreground",
    secondary: "bg-secondary text-secondary-foreground",
    outline: "text-foreground border border-input",
    destructive: "bg-destructive text-destructive-foreground",
  };
  return (
    <div className={cn("inline-flex items-center rounded-full border border-transparent px-2.5 py-0.5 text-xs font-bold uppercase tracking-wider transition-colors", variants[variant], className)} {...props} />
  );
};

